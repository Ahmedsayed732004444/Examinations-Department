$options = [
    ['text' => 'نعم', 'score' => 2],
    ['text' => 'إلى حد ما', 'score' => 1],
    ['text' => 'لا', 'score' => 0]
];

foreach ($data as $assessmentTitle => $dimensionsData) {
    $assessment = Assessment::where('title_ar', $assessmentTitle)->first();
    if (!$assessment) {
        echo "Assessment '$assessmentTitle' not found!\n";
        continue;
    }

    // Delete existing questions if any (to avoid duplicates)
    $assessment->questions()->delete();

    $qOrder = 0;
    $dimensions = $assessment->dimensions()->orderBy('order_index')->get();
    $questionsArr = array_values($dimensionsData);

    foreach ($dimensions as $index => $dimension) {
        if (!isset($questionsArr[$index])) {
            echo "No questions provided for dimension index $index in assessment '$assessmentTitle'!\n";
            continue;
        }
        $questions = $questionsArr[$index];

        foreach ($questions as $qText) {
            $question = Question::create([
                'assessment_id' => $assessment->id,
                'dimension_id' => $dimension->id,
                'text_ar' => $qText,
                'order_index' => $qOrder++
            ]);

            foreach ($options as $optIndex => $optData) {
                AnswerOption::create([
                    'question_id' => $question->id,
                    'label_ar' => $optData['text'],
                    'score_value' => $optData['score'],
                    'order_index' => $optIndex
                ]);
            }
        }
    }
    echo "Seeded questions for '$assessmentTitle'\n";
}
echo "Done.\n";
