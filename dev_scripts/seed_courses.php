<?php

use App\Models\Assessment;

$categories = [
    'التوجيه والتوافق المهني' => 'مهارات القيادة الإدارية، تحفيز الموظفين وإدارة الأداء، تطوير المسار الوظيفي، استراتيجيات التوجيه المهني',
    'الذات والشحصيه' => 'تنمية الذكاء العاطفي، بناء الثقة بالنفس وتطوير الذات، التفكير الإيجابي وإدارة الانفعالات، اكتشاف الذات وتحليل الشخصية',
    'الكفاءة الشخصية والنجاح المهني' => 'إدارة الوقت والأولويات، مهارات اتخاذ القرار وحل المشكلات، التخطيط الاستراتيجي الشخصي، الابتكار والإبداع الإداري',
    'مقاييس الاتصال والعلاقات المهنية' => 'مهارات التواصل الفعال، فن التفاوض والإقناع، بناء وقيادة فرق العمل، إدارة العلاقات المهنية',
    'مقاييس الصحةاملهنية' => 'إدارة ضغوط العمل، الوقاية من الاحتراق الوظيفي، أساسيات السلامة والصحة المهنية، تحقيق التوازن بين العمل والحياة',
    'مقاييس التوجيه والتوافق المهني' => 'مهارات القيادة الإدارية، تحفيز الموظفين وإدارة الأداء، تطوير المسار الوظيفي، استراتيجيات التوجيه المهني',
];

$assessments = Assessment::all();
$count = 0;
foreach ($assessments as $assessment) {
    $cat = $assessment->category;
    if (isset($categories[$cat])) {
        $assessment->programs_ar = $categories[$cat];
        $assessment->save();
        $count++;
    }
}

echo "Updated programs for $count assessments.\n";
